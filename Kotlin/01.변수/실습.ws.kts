// 01. 변수

// 주석 작성하는 방법
// 주석 -> 사람만 보기 위해서 작성

// 1. var 변수 선언
var changeYes = 100
println(changeYes)
//var changeYes // 선언과 동시에 초기화를 시켜줘야함

changeYes = 200
println(changeYes)

// 2. val 변수 선언
val CHANGE_NO = 100
println(CHANGE_NO)
//CHANGE_NO = 200 //val cannot be reassigned

val SCHOOL_NAME = "대한민국 학교"
println(SCHOOL_NAME)
